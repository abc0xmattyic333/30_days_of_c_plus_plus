// C++ Statements

// Statements are the building blocks of a C++ program. 
// They are executed sequentially, one after another, in the order they are written. 
//A statement can be an expression, a declaration, or a control flow statement.

// A computer program is a collection of instructions to be executed by a computer.

// In a programming language, these programming instructions are called statements.

cout << "Hello World"; // C++ statement

// Many statements

// Most C++ programs contain many statements.

// The statements are executed, one by one, in the same order as they are written.


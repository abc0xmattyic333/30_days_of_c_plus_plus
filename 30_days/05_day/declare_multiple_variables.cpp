// C++ Declare Multiple Variables

// Declare Many Variables

// To declare more than one variable of the same type, you can use a comma-separated list of variable names followed by the type name.

// Example

int x = 5, y = 6, z = 50;
cout << x + y + z;

// In this example, x, y, and z are all integers. The value of x is 5, the value of y is 6, and the value of z is 50. 
// The program prints the sum of x, y, and z, which is 61.

// One Value to Multiple Variables

// You can also assign the same value to multiple variables in a single statement.

// Example

int x, y, z;
x = y = z = 50;
cout << x + y + z;


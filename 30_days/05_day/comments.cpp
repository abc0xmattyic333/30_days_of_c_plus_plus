// C++ Comments

/*
Comments can be used to explain C++ code, and to make it more readable. 
It can also be used to prevent execution when testing alternative code.
Comments can be single-line or multi-line.
*/

// Single-line comments start with two forward slashes (//).

// This is a single-line comment

cout << "Hello, World!"; // prints Hello, World!

// Multi-line comments start with a forward slash and an asterisk (/*) and end with an asterisk and a forward slash (*/).

/*
This is a multi-line comment
-
-
-
-
*/

// Comments can be placed anywhere in the code. They can also be used to comment out code that you do not want to run.

// Single or multi-line comments?
// It is up to you to decide whether you want to use single-line or multi-line comments.